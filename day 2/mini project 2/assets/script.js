// 1st Store the temperature in Celsius
let celsius = 25; 

// 2nd Convert Celsius to Fahrenheit 
let fahrenheit = (celsius * 9 / 5) + 32; 

// 3rd  Store the converted Fahrenheit value in a variable
let convertedTemp = fahrenheit; 

// 4th  Display the result in the console 
console.log("Temperature Converter");
console.log("*************************");
console.log("Celsius:", celsius, "°C");
console.log("Fahrenheit:", convertedTemp, "°F");
console.log("*************************");
